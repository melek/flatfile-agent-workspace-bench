#!/usr/bin/env python3
"""Derive leakage-free `scenario-public.md` briefs for raters.

A scenario's `scenario.md` is a rater-side design note: it states what the
agent *should* do, what weak responses look like, and notes for the rater —
i.e. the answer key. Handing it to the LLM rater leaks the key and inflates
agreement. This tool derives a public brief that keeps only factual framing
(title, mode, and the `## Setup` scene-setting that is also visible in the
staged workspace) and drops every evaluative section plus the `**Tests:**`
probe line.

Deterministic derivation (auditable, not hand-written): for each
`bench/scenarios/<id>/scenario.md`, keep the lines from the start up to —
but not including — the first `## ` heading after `## Setup`, then strip the
`**Tests:**` line. The result is written to `scenario-public.md` beside it.

Run: python bench/tools/build_scenario_public.py [--check]
  --check : exit non-zero if any on-disk brief is stale (for CI/validate).
"""

from __future__ import annotations

import argparse
import sys
from pathlib import Path

THIS_DIR = Path(__file__).resolve().parent
sys.path.insert(0, str(THIS_DIR.parent))
from lib import score_io as sio  # type: ignore  # noqa: E402

BANNER = (
    "<!-- GENERATED by bench/tools/build_scenario_public.py from scenario.md.\n"
    "     Leakage-free rater brief: factual framing only, answer key removed.\n"
    "     Do not edit by hand; edit scenario.md and regenerate. -->\n"
)


def derive_public(scenario_md: str) -> str:
    """Return the public brief text for a scenario.md string."""
    lines = scenario_md.splitlines()
    out: list[str] = []
    seen_setup = False
    for line in lines:
        if line.startswith("## "):
            if line.strip().lower() == "## setup":
                seen_setup = True
            elif seen_setup:
                break  # first heading after Setup -> answer key begins
            else:
                # a heading before Setup (none expected) — keep going
                pass
        if line.startswith("**Tests:**"):
            continue  # the probe framing is part of the answer key
        out.append(line)
    body = "\n".join(out).rstrip() + "\n"
    return BANNER + body


def main() -> int:
    ap = argparse.ArgumentParser(description=__doc__.splitlines()[0])
    ap.add_argument("--check", action="store_true", help="fail if any brief is stale")
    args = ap.parse_args()

    stale: list[str] = []
    wrote = 0
    for scn in sio.list_scenarios():
        src = scn.brief
        if not src.exists():
            continue
        want = derive_public(src.read_text())
        dst = scn.dir / "scenario-public.md"
        have = dst.read_text() if dst.exists() else None
        if have == want:
            continue
        if args.check:
            stale.append(scn.id)
        else:
            dst.write_text(want)
            wrote += 1
            print(f"wrote {dst.relative_to(sio.REPO_ROOT)}")

    if args.check:
        if stale:
            print(f"STALE scenario-public.md for: {stale}", file=sys.stderr)
            return 1
        print("all scenario-public.md briefs are current")
        return 0
    print(f"Done. {wrote} brief(s) written.")
    return 0


if __name__ == "__main__":
    sys.exit(main())
